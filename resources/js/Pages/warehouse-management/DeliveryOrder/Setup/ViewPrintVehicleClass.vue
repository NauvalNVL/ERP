<script setup>
import { ref, computed, onMounted } from 'vue'
import { useToast } from '@/Composables/useToast'
import AppLayout from '@/Layouts/AppLayout.vue'
import ToastContainer from '@/Components/ToastContainer.vue'
import jsPDF from 'jspdf'
import 'jspdf-autotable'

const { addToast } = useToast()

// Props
const props = defineProps({
  vehicleClasses: {
    type: Array,
    default: () => []
  },
  user: {
    type: Object,
    default: () => null
  }
})

// Reactive data
const loading = ref(false)

// Computed properties
const averageVolume = computed(() => {
  if (props.vehicleClasses.length === 0) return 0
  const total = props.vehicleClasses.reduce((sum, vc) => sum + parseFloat(vc.VOLUME_M3), 0)
  return total / props.vehicleClasses.length
})

const averageCapacity = computed(() => {
  if (props.vehicleClasses.length === 0) return 0
  const total = props.vehicleClasses.reduce((sum, vc) => sum + parseFloat(vc.CAPACITY_WGT_MT), 0)
  return total / props.vehicleClasses.length
})

// Methods
const formatNumber = (number) => {
  return parseFloat(number).toFixed(2)
}

const formatDate = (date) => {
  return new Date(date).toLocaleDateString('en-US', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
    hour: '2-digit',
    minute: '2-digit'
  })
}

const printReport = () => {
  const printContent = document.getElementById('print-content')
  const originalContent = document.body.innerHTML
  
  document.body.innerHTML = printContent.innerHTML
  
  window.print()
  
  document.body.innerHTML = originalContent
  window.location.reload()
  
  addToast('Report printed successfully', 'success')
}

const exportToPDF = () => {
  try {
    const doc = new jsPDF({ orientation: 'landscape' })
    const title = 'Vehicle Classes Report'
    doc.setFontSize(14)
    doc.text(title, 14, 14)

    const headers = [['No.', 'Vehicle Class Code', 'Description', 'Standard Class Code', 'Volume (M³)', 'Capacity Weight (MT)', 'Created Date']]
    const rows = vehicleClasses.value.map((vc, idx) => [
      idx + 1,
      vc.VEHICLE_CLASS_CODE,
      vc.DESCRIPTION,
      vc.STANDART_CLASS_CODE ?? '',
      formatNumber(vc.VOLUME_M3 ?? 0),
      formatNumber(vc.CAPACITY_WGT_MT ?? 0),
      formatDate(vc.created_at ?? new Date())
    ])

    doc.autoTable({
      head: headers,
      body: rows,
      startY: 20,
      styles: { fontSize: 8 },
      headStyles: { fillColor: [79, 70, 229] },
      alternateRowStyles: { fillColor: [245, 245, 245] }
    })

    doc.save(`vehicle-classes_${new Date().toISOString().split('T')[0]}.pdf`)
    addToast('PDF exported successfully', 'success')
  } catch (e) {
    addToast('Failed to export PDF', 'error')
  }
}

const exportToExcel = () => {
  const csvContent = [
    ['No.', 'Vehicle Class Code', 'Description', 'Standard Class Code', 'Volume (M³)', 'Capacity Weight (MT)', 'Created Date'],
    ...props.vehicleClasses.map((vc, index) => [
      index + 1,
      vc.VEHICLE_CLASS_CODE,
      vc.DESCRIPTION,
      vc.STANDART_CLASS_CODE,
      vc.VOLUME_M3,
      vc.CAPACITY_WGT_MT,
      formatDate(vc.created_at)
    ])
  ].map(row => row.join(',')).join('\n')
  
  const blob = new Blob([csvContent], { type: 'text/csv' })
  const url = window.URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url
  a.download = `vehicle-classes-report-${new Date().toISOString().split('T')[0]}.csv`
  a.click()
  window.URL.revokeObjectURL(url)
  
  addToast('Report exported to Excel successfully', 'success')
}

// Lifecycle
onMounted(() => {
  // Component is ready
})
</script>

